    public boolean validateEmpty()
    {
      String firstName= txtProdName.getText();
        //System.out.println(firstName);
        if(firstName.trim().isEmpty()
                ||(txtProdDesc.getText().trim().isEmpty())
                ||(txtVendorName.getText().trim().isEmpty())
                ||(txtModelNumber.getText().trim().isEmpty())
                ||(txtPrice.getText().trim().isEmpty())
                ||(txtProdFeature.getText().trim().isEmpty()))
               
               // ||JYear.getText().isEmpty())
        {
          JOptionPane.showMessageDialog(null,"Fields cannot be blank");
          return true;
        }
        else
        {
            
            return false;
        }   
    }
    public void create()
    {
        String prodName = txtProdName.getText();
        String prodDesc = txtProdDesc.getText();
        String modelNumber = txtModelNumber.getText();
        String vendorName = txtVendorName.getText();
        
       //String test = vendor.getVendorName();
       //System.out.println(test);
       //// cprice = Double.parseDouble(txtCeilPrice.getText());
       // fprice = Double.parseDouble(txtFloorPrice.getText());
       //String vendorName = txtVendorName.getText();
        String prodFeature = txtProdFeature.getText();
        String item= (String)typeCombo.getSelectedItem();
        
        Product product = productCatalog.addProduct();
        
        product.setProdName(prodName);
        product.setBasePrice(price);
        product.setModelNumber(modelNumber);
        product.setProdDesc(prodDesc);
        product.setVendorName( vendorName);
        product.setProdFeature(prodFeature);
        product.setType(item);
        product.setCeilPrice(cprice);
        product.setFloorPrice(fprice);
        
        JOptionPane.showMessageDialog(null,"Product added successfully!!");
        clear();
        
    }
