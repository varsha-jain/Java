    private void txtProdNameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtProdNameFocusLost
        // TODO add your handling code here:
        String fname=txtProdName.getText();
           
            int i;
        for(i=0; i<fname.length();i++)
        {
            if(fname.charAt(i)=='*' //if first name contains special characters
                    ||fname.charAt(i)=='/'
                    ||fname.charAt(i)=='@'
                    ||fname.charAt(i)=='!'
                    ||fname.charAt(i)=='#'
                    ||fname.charAt(i)=='%'
                    ||fname.charAt(i)=='$'
                    ||fname.charAt(i)=='('
                    ||fname.charAt(i)==')'
                    ||fname.charAt(i)=='_'
                    ||fname.charAt(i)=='.'
                    ||txtProdName.getText().trim().isEmpty()
                    )
                {
              JOptionPane.showMessageDialog(null,"You cannot enter special characters or whitespaces!");
              txtProdName.setText("");
              break;
             
            }
        }
    }//GEN-LAST:event_txtProdNameFocusLost

