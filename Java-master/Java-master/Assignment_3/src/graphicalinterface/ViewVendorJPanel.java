public boolean validateEmpty()
    {
      String firstName= txtVendorName.getText();
        //System.out.println(firstName);
        if(firstName.trim().isEmpty()
                ||(txtVendorEmail.getText().trim().isEmpty())
                )
               
               // ||JYear.getText().isEmpty())
        {
          JOptionPane.showMessageDialog(null,"Fields cannot be blank");
          return true;
        }
        else
        {
            
            return false;
        }   
    }
private void updateDetails() {
        
        vendor.setVendorName(txtVendorName.getText());
        vendor.setVendorMail(txtVendorEmail.getText());
        vendor.setVendorID(txtVendorID.getText());
        btnSave.setEnabled(false);
        btnUpdateVendor.setEnabled(true);
        JOptionPane.showMessageDialog(null,"Vendor Details updated succefully!");
        clear(); 
    }
