public void populateTable() 
    {
        DefaultTableModel dtm=(DefaultTableModel)tblVendor.getModel();
        dtm.setRowCount(0);
        for(Vendor vendor : vendorCatalog.getVendor())
        {
            Object obj[]= new Object[2];
            obj[0]=vendor;
            obj[1]=vendor.getVendorID();
            dtm.addRow(obj);
        }
    }
    private void btnViewVendorDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewVendorDetailsActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblVendor.getSelectedRow();
        if(selectedRow<0)
        {
          JOptionPane.showMessageDialog(null,"Please select any row!","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
        Vendor prod = (Vendor)tblVendor.getValueAt(selectedRow,0);
        ViewVendorJPanel viewProduct= new ViewVendorJPanel (userProcessContainer, prod);
        userProcessContainer.add("ViewVendorJPanel",viewProduct);
        CardLayout layout=(CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer); 
        }
    }//GEN-LAST:event_btnViewVendorDetailsActionPerformed

